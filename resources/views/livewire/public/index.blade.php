            <div x-data="{
                // Slides array
                slides: @js($chiffres),
            
                // Content Slider options
                arrowsNavigation: false,
                dotsNavigation: '', // '' for no dots navigation, 'after' or 'inner'
                transition: 'slide', // '' for no transition, 'fade', 'slide', 'slide-y', 'blur', 'zoom'
                loop: true,
