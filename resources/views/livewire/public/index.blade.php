                                    'opacity-0': transition === 'fade' && currentIndex !== index,
                                    'blur-xl opacity-0': transition === 'blur' && currentIndex !== index,
                                    'translate-x-0': transition === 'slide' && currentIndex === index,
                                    '-translate-x-full': transition === 'slide' && currentIndex > index,
